// Helper function to get a specific Pokemon by ID
function getGen9PokemonById(id) {
    return GEN9_POKEMON.find(pokemon => pokemon.id === id);
}

// Helper function to get Pokemon by name
function getGen9PokemonByName(name) {
    return GEN9_POKEMON.find(pokemon => pokemon.name.toLowerCase() === name.toLowerCase());
}

// Helper function to get all Pokemon of a specific type
function getGen9PokemonByType(type) {
    return GEN9_POKEMON.filter(pokemon => pokemon.types.includes(type.toLowerCase()));
}

