// Helper function to get a specific Pokemon by ID
function getGen7PokemonById(id) {
    return GEN7_POKEMON.find(pokemon => pokemon.id === id);
}

// Helper function to get Pokemon by name
function getGen7PokemonByName(name) {
    return GEN7_POKEMON.find(pokemon => pokemon.name.toLowerCase() === name.toLowerCase());
}

// Helper function to get all Pokemon of a specific type
function getGen7PokemonByType(type) {
    return GEN7_POKEMON.filter(pokemon => pokemon.types.includes(type.toLowerCase()));
}

