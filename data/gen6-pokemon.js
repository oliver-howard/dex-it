// Helper function to get a specific Pokemon by ID
function getGen6PokemonById(id) {
    return GEN6_POKEMON.find(pokemon => pokemon.id === id);
}

// Helper function to get Pokemon by name
function getGen6PokemonByName(name) {
    return GEN6_POKEMON.find(pokemon => pokemon.name.toLowerCase() === name.toLowerCase());
}

// Helper function to get all Pokemon of a specific type
function getGen6PokemonByType(type) {
    return GEN6_POKEMON.filter(pokemon => pokemon.types.includes(type.toLowerCase()));
}

