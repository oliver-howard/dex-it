// Helper function to get a specific Pokemon by ID
function getGen3PokemonById(id) {
    return GEN3_POKEMON.find(pokemon => pokemon.id === id);
}

// Helper function to get Pokemon by name
function getGen3PokemonByName(name) {
    return GEN3_POKEMON.find(pokemon => pokemon.name.toLowerCase() === name.toLowerCase());
}

// Helper function to get all Pokemon of a specific type
function getGen3PokemonByType(type) {
    return GEN3_POKEMON.filter(pokemon => pokemon.types.includes(type.toLowerCase()));
}

