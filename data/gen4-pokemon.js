// Helper function to get a specific Pokemon by ID
function getGen4PokemonById(id) {
    return GEN4_POKEMON.find(pokemon => pokemon.id === id);
}

// Helper function to get Pokemon by name
function getGen4PokemonByName(name) {
    return GEN4_POKEMON.find(pokemon => pokemon.name.toLowerCase() === name.toLowerCase());
}

// Helper function to get all Pokemon of a specific type
function getGen4PokemonByType(type) {
    return GEN4_POKEMON.filter(pokemon => pokemon.types.includes(type.toLowerCase()));
}

