// Helper function to get a specific Pokemon by ID
function getGen5PokemonById(id) {
    return GEN5_POKEMON.find(pokemon => pokemon.id === id);
}

// Helper function to get Pokemon by name
function getGen5PokemonByName(name) {
    return GEN5_POKEMON.find(pokemon => pokemon.name.toLowerCase() === name.toLowerCase());
}

// Helper function to get all Pokemon of a specific type
function getGen5PokemonByType(type) {
    return GEN5_POKEMON.filter(pokemon => pokemon.types.includes(type.toLowerCase()));
}

