// Helper function to get a specific Pokemon by ID
function getGen2PokemonById(id) {
    return GEN2_POKEMON.find(pokemon => pokemon.id === id);
}

// Helper function to get Pokemon by name
function getGen2PokemonByName(name) {
    return GEN2_POKEMON.find(pokemon => pokemon.name.toLowerCase() === name.toLowerCase());
}

// Helper function to get all Pokemon of a specific type
function getGen2PokemonByType(type) {
    return GEN2_POKEMON.filter(pokemon => pokemon.types.includes(type.toLowerCase()));
}

